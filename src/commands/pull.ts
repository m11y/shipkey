import { Command } from "commander";
import { getBackend } from "../backends";
import { loadConfig } from "../config";
import { walkDirsWithShipkey } from "../scanner";
import { readFile, writeFile } from "fs/promises";
import { resolve, join, relative } from "path";

export const pullCommand = new Command("pull")
  .description("Pull keys from your password manager and generate env files")
  .option("-e, --env <env>", "environment (overrides shipkey.json)")
  .option("--vault <vault>", "Vault or folder name", "shipkey")
  .option("--envrc", "generate .envrc (direnv)")
  .option("--dev-vars", "generate .dev.vars for Cloudflare Workers")
  .argument("[dir]", "project directory", ".")
  .action(async (dir: string, opts) => {
    const projectRoot = resolve(dir);
    const vault = opts.vault;

    const shipkeyDirs = await walkDirsWithShipkey(projectRoot);

    if (shipkeyDirs.length === 0) {
      console.error(
        "  No shipkey.json found. Run `shipkey scan` first."
      );
      process.exit(1);
    }

    for (const d of shipkeyDirs) {
      const relDir = relative(projectRoot, d) || ".";
      console.log(`\n  [${relDir}]`);

      let config;
      try {
        config = await loadConfig(d);
      } catch {
        console.error(`  ✗ Could not read shipkey.json in ${relDir}`);
        continue;
      }

      const backend = getBackend(config.backend);
      const env = opts.env ?? config.env ?? "dev";
      if (!(await backend.isAvailable())) {
        console.error(
          `  ✗ ${backend.name} CLI not available. Run 'shipkey setup' for installation instructions.`
        );
        continue;
      }

      console.log(`  Pulling keys for ${config.project}.${env}...`);

      const refs = await backend.list(config.project, env);

      if (refs.length === 0) {
        console.log(`  No keys found for ${config.project}.${env} in vault ${vault}.`);
        continue;
      }

      const entries: { key: string; value: string; ref: typeof refs[0] }[] = [];
      for (const ref of refs) {
        try {
          const value = await backend.read(ref);
          entries.push({ key: ref.field, value, ref });
          console.log(`  ✓ Restored ${ref.field}`);
        } catch (err) {
          console.error(
            `  ✗ ${ref.provider}/${ref.field} — ${err instanceof Error ? err.message : err}`
          );
        }
      }

      if (entries.length === 0) continue;

      // Update .env in-place: preserve structure, comments, ordering.
      // Only fill values for keys found in the password manager.
      const envPath = join(d, ".env");
      const pulledKeys = new Map(entries.map((e) => [e.key, e.value]));
      const MARKER = "# generated by shipkey";

      let updatedCount = 0;
      let newCount = 0;
      let outputLines: string[];

      try {
        const existing = await readFile(envPath, "utf-8");
        const lines = existing.split("\n");
        const seen = new Set<string>();

        outputLines = lines.map((line) => {
          const trimmed = line.trim();
          if (!trimmed || trimmed.startsWith("#")) return line;
          const eqIdx = trimmed.indexOf("=");
          if (eqIdx === -1) return line;
          const key = trimmed.slice(0, eqIdx);
          if (pulledKeys.has(key)) {
            seen.add(key);
            updatedCount++;
            return `${key}=${pulledKeys.get(key)} ${MARKER}`;
          }
          // Strip old marker from keys no longer in password manager
          return line.replace(/ # generated by shipkey$/, "");
        });

        // Append keys from password manager that don't exist in local file
        const newKeys = entries.filter((e) => !seen.has(e.key));
        if (newKeys.length > 0) {
          // Ensure there's a blank line before new keys
          if (outputLines.length > 0 && outputLines[outputLines.length - 1]?.trim() !== "") {
            outputLines.push("");
          }
          for (const e of newKeys) {
            outputLines.push(`${e.key}=${e.value} ${MARKER}`);
            newCount++;
          }
        }
      } catch {
        // .env doesn't exist yet — create from scratch
        outputLines = entries.map((e) => `${e.key}=${e.value} ${MARKER}`);
        outputLines.push("");
        updatedCount = entries.length;
      }

      await writeFile(envPath, outputLines.join("\n"));
      const parts = [`${updatedCount} updated`];
      if (newCount > 0) parts.push(`${newCount} new`);

      // Fill in defaults from shipkey.json for keys not yet in .env
      let defaultsCount = 0;
      const defaults = config.defaults;
      if (defaults && Object.keys(defaults).length > 0) {
        const content = await readFile(envPath, "utf-8");
        const existingKeys = new Set<string>();
        for (const line of content.split("\n")) {
          const trimmed = line.trim();
          if (!trimmed || trimmed.startsWith("#")) continue;
          const eqIdx = trimmed.indexOf("=");
          if (eqIdx !== -1) existingKeys.add(trimmed.slice(0, eqIdx));
        }

        const missingDefaults = Object.entries(defaults).filter(
          ([key]) => !existingKeys.has(key)
        );

        if (missingDefaults.length > 0) {
          const lines = content.split("\n");
          if (lines.length > 0 && lines[lines.length - 1]?.trim() !== "") {
            lines.push("");
          }
          for (const [key, value] of missingDefaults) {
            lines.push(`${key}=${value}`);
            defaultsCount++;
          }
          await writeFile(envPath, lines.join("\n"));
        }
      }

      if (defaultsCount > 0) parts.push(`${defaultsCount} defaults`);
      console.log(`  ✓ .env (${parts.join(", ")})`);

      if (opts.envrc) {
        const envrcContent = [
          `# Auto-generated by shipkey — do not edit manually`,
          `# Project: ${config.project}  Environment: ${env}`,
          "",
          ...entries.map((e) => {
            const inlineRef = backend.buildInlineRef?.(e.ref);
            if (inlineRef) {
              return `export ${e.key}=$(op read "${inlineRef}")`;
            }
            return `export ${e.key}="${e.value}"`;
          }),
          "",
        ].join("\n");

        await writeFile(join(d, ".envrc"), envrcContent);
        console.log(`  ✓ Generated .envrc`);
      }

      if (opts.devVars) {
        // Only include keys listed in cloudflare targets, fall back to all keys
        const cfKeys = new Set<string>();
        const cfTargets = config?.targets?.cloudflare;
        if (cfTargets) {
          for (const keys of Object.values(cfTargets)) {
            if (Array.isArray(keys)) {
              for (const k of keys) cfKeys.add(k);
            }
          }
        }

        const devVarsEntries = cfKeys.size > 0
          ? entries.filter((e) => cfKeys.has(e.key))
          : entries;

        if (devVarsEntries.length > 0) {
          const devVarsContent = [
            `# Auto-generated by shipkey — do not edit manually`,
            `# Project: ${config.project}  Environment: ${env}`,
            "",
            ...devVarsEntries.map((e) => `${e.key}=${e.value}`),
            "",
          ].join("\n");

          await writeFile(join(d, ".dev.vars"), devVarsContent);
          console.log(`  ✓ Generated .dev.vars (${devVarsEntries.length} keys)`);
        }
      }

      console.log(`  Restored ${entries.length} keys for ${config.project}.${env}`);
    }
  });
